package org.jolokia.extra.addon.scripting.factory;

import org.jolokia.extra.addon.scripting.model.Meta;
import org.jolokia.extra.addon.scripting.exception.ValidationException;
import org.json.simple.JSONObject;

/**
 * Factory to allow the creation of a Conditional from an JSON Objects.
 */
public class MetaFactory {

    public MetaFactory()  {
    }

    public Meta createMetaFromJson(Object rawMeta) throws ValidationException {
        try {
            if(rawMeta instanceof JSONObject) {
                Meta meta = new Meta();
                meta.putAll((JSONObject) rawMeta);
                return meta;
            }

            throw new ValidationException("Meta is of wrong type. Expected a JSONObject, but it is of type: " + rawMeta.getClass());
        } catch (Exception e) {
            throw new ValidationException("Failed to create Meta instance from JSON: " + rawMeta, e);
        }
    }
}
